    private void saveUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveUserActionPerformed
        // TODO add your handling code here:
        try {
            String query = "insert into voucher values(?,?,?, ?)";
            pat = con.prepareStatement(query);
            pat.setString(2, inClientName.getText());
            pat.setInt(3, Integer.parseInt(inAmount.getText()));
            pat.setString(4, inDateIssue.getText());

            pat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Voucher Created");

            pat.close();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        fetch();
        inVoucherID.setText("");
        inClientName.setText("");
        inAmount.setText("");
        inDateIssue.setText("");
    }//GEN-LAST:event_saveUserActionPerformed

